<template>
  <div class="images">
    <div class="row-block">
      <f-button styles="red" @onClick="$router.push('/dashboard/images/create')">افزودن ایمیج</f-button>
    </div>
    <div class="table-title">ایمیج های شما</div>
    <vue-good-table :columns="header" :rows="images" :rtl="true" styleClass="vgt-table">
      <template slot="table-row" slot-scope="props">
        <span v-if="props.column.field == 'action'">
          <action-button class="action-button-s" @onClick="versions(props.row)">
           <img src='/icons/ic-time.svg'/><span> ورژن ها </span>
          </action-button>
          <action-button class="action-button-s" @onClick="createVersion(props.row)">
            <img src='/icons/ic-upload.svg' /> <span>آپلود</span>
          </action-button>
          <action-button class="action-button-s" @onClick="remove(props.row)">
            <img src="/icons/ic-delete.svg">
            <span>حذف</span>
          </action-button>
        </span>
        <span v-else>{{props.formattedRow[props.column.field]}}</span>
      </template>
    </vue-good-table>
  </div>
</template>

<script>
import FTable from "~/components/Dashboard/table";
import FButton from "~/components/elements/button";
import FDate from "~/utils/date";
import Alert from "~/components/Dashboard/alert";
import ActionButton from "~/components/Dashboard/table/action-button";

export default {
  layout: "dashboard",
  async asyncData({ store, route, redirect }) {
    try {
      await store.dispatch("getImages");
    } catch (e) {
      if (e.status === 401) {
        redirect("/user/login");
      }
    }
  },
  data() {
    return {
      header: [
        {
          label: "نام دامنه",
          field: "name"
        },
        {
          label: "تاریخ ساخت ایمیج",
          field: "created_at"
        },
        {
          label: "آخرین ورژن",
          field: "version"
        },
        {
          label: "آخرین به روز‌رسانی",
          field: "date"
        },
        {
          label: "مدیریت",
          field: "action",
          html: true
        }
      ]
    };
  },
  components: {
    FTable,
    FButton,
    ActionButton
  },
  computed: {
    images() {
      let images = this.$store.state.images;
      if (images) {
        return images.map(({ created_at, name, last_version }) => {
          if (last_version) {
            return {
              name,
              created_at: FDate({ date: created_at }),
              version: last_version.version,
              date: FDate({ date: last_version.date })
            };
          } else {
            return {
              name,
              created_at: FDate({ date: created_at })
            };
          }
        });
      }
    },
  },
  methods: {
    versions({name}) {
      this.$router.push(`/dashboard/images/${name}/versions`);
    },
    createVersion({name}) {
      this.$router.push(`/dashboard/images/${name}/versions/create`);
    },
    remove({name}) {
      this.$alertify(
        {
          title: "ایمیج حذف شود؟",
          description: " آیا از حذف شدن ایمیج خود مطمئن هستید؟"
        },
        status => {
          if (status) {
            this.$store
              .dispatch("deleteImage", name)
              .then(res => {
                this.$store.dispatch("getImages");
                this.$notify({
                  title: res.message,
                  type: "success"
                });
              })
              .catch(e => {
                this.$notify({
                  title: e.message,
                  type: "error"
                });
              });
          }
        }
      );
    }
  }
};
</script>

